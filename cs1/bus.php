	<h2>Unconfirmed Info</h2>

<p>
This header is unconfirmed info across the internet, please be aware when reading.
</p>

<p>
Bus depots should be placed on arterial roads where they get good access for the whole city. Don't put depot into some back corner of residential local road connection. However DON'T put bus depot directly to arterial road because buses cannot come back to the depot crossing the six lanes, so actually put the depot into local road RIGHT after connection to collector road and arterial road intersection. Got all that? Good <img src="https://www.pmctactical.org/images/smileys/icon_smile.gif" alt="Smiley :)" loading="lazy">
</p>

<p>
On huge cities it would be good idea to place second bus depot so buses when departing on a line don't have to travel across the whole city to reach that line.
</p>

<p>
Long bus line should have fewer stops, each stop increases the time it takes to travel this line, if the time is too long citizens will choose private car instead. Short lines for specific areas are fine with frequent stops.
</p>

<p>
Transport HUBs where lets say train, metro and bus lines are merging. These are effective I've read.
</p>

