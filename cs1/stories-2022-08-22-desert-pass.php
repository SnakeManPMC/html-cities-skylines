2022-08-23T13:28:00Z When zoning high density residential in big mass blob blocks for example road grid 30 units x 10 units blocks several of them, maybe 5 x 3 blocks (get it? 30u * 10u * 5 * 3, yes? hehe), that generates over hundred households and who knows how many individual persons. This causes these people to move into the buildings by arriving in some manner, usually by private cars through highway outside connection. Now it doesn't seem related in any ways but if you mass blob zone like this you can TEMPORARILY cripple your industry districts nearby or connected to the same highway intersection. Trust me this has happened to me in several terrain savegames. My advice is to only zone one 30u * 10u road grid block at the time and wait until you see a convoy of private cars driving into the buildings and people going in. <i>Later Edit: or do it right in the first place: intercity bus station, passenger train station or passenger ship harbor, connected to the new high density residential zone(s) with metro subway lines. These will pretty much eliminate all those private car convoys rushing to the citys new zones.</i>
