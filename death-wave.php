	<h2>Death Wave Cities Skylines</h2>

<p>
Death Wave is what happens when many citizens in your city die all at once. Nobody has been able to find a permanent proper fix for this, its just one of those game mechanics you have to deal with.
</p>

<p>
What happens is your cemeteries and crematorioums are overwhelmed with dead persons aka bodies, corpses, in buildings across the city. Especially if your <a href="traffic-jam.php">traffic flow</a> is bad this will lead into population abandoning those buildings due rotting corpses.
</p>

<p>
Once your city grows, at some point initial population reaches old age and starts to die off pretty much at the same time putting huge pressure on health services (cemeteries and crematoriums), hearses are driving like crazy and cannot keep up with the walking zombies infestation of rotting corpses <img src="../images/smileys/icon_smile.gif" alt="Smiley :)" loading="lazy">
</p>

<p>
Some people write that death wave can be avoided by growing your residential zones in small areas instead of pausing game and maxing out roads / residential zones as much your grids or money allows. Not sure if this is true, I've tried to grow my city slowly but it still gets insane death wave.
</p>

<p>
Easiest way to prevent death wave is to use <a href="mod-lifecycle-rebalanced.php">Lifecycle Rebalanced</a> addon. With this mod you have no problems with death waves. Sure you still sometimes get bunch of skulls and bones icons floating in buildings over the horizon, but crematorium hearses can easily handle those.
</p>

<p>
Quote from my <a href="stories-2022-08-19-green-peaks.php">Stories 2022-08-19 Green Peaks</a> savegame: <i>We had a slight death wave and while crematoriums had capacity, it was well on the green with deceased 290 and capacity 420, we still got plenty of red skullz following abandoned buildings. All crematoriums were running 7/7 hearses so they were maxed out.</i>
</p>

<p>
You need crematoriums everywhere, building is a building, I had several people dying in COMMERCIAL zone soon after its grand opening, so dying and dead persons piling up is not only a residential zone problem.
